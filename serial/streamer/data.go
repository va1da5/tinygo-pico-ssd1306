package main

import "time"

type Animation struct {
	Frames  [][]byte
	Delay   time.Duration
	current int
}

func (animation *Animation) Next() []byte {
	frame := animation.Frames[animation.current]
	animation.current++
	if animation.current >= len(animation.Frames) {
		animation.current = 0
	}
	return frame
}

